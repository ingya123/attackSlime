package miniProject.image;

import javafx.scene.image.ImageView;

public class CharImage {
	
	private ImageView basics;
	private ImageView attack;
	private ImageView powerAttack;
	public ImageView getBasics() {
		return basics;
	}
	public void setBasics(ImageView basics) {
		this.basics = basics;
	}
	public ImageView getAttack() {
		return attack;
	}
	public void setAttack(ImageView attack) {
		this.attack = attack;
	}
	public ImageView getPowerAttack() {
		return powerAttack;
	}
	public void setPowerAttack(ImageView powerAttack) {
		this.powerAttack = powerAttack;
	}
	
	
	
	
	
}